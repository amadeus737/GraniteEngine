void Stein::Input()
{
	if (GraniteKeyboard.KeyIsPressed('W') || GraniteKeyboard.KeyIsPressed(VK_UP))
		_player.Move(_playerFwdVelocity);

	if (GraniteKeyboard.KeyIsPressed('S') || GraniteKeyboard.KeyIsPressed(VK_DOWN))
		_player.Move(_playerBwdVelocity);

	if (GraniteKeyboard.KeyIsPressed('A') || GraniteKeyboard.KeyIsPressed(VK_LEFT))  _player.Rotate(-_deltaTime);

	if (GraniteKeyboard.KeyIsPressed('D') || GraniteKeyboard.KeyIsPressed(VK_RIGHT)) _player.Rotate(_deltaTime);

	if (GraniteKeyboard.KeyIsPressed('M')) 
		_wantsToDraw2Dmap = true;

	if (GraniteKeyboard.KeyIsPressed('E'))
		_wantsToOpenDoor = true;
}

void Stein::Update()
{
	// Check walls for collisions
	bool isDoor;
	int fwd_x, fwd_y, back_x, back_y, doorIndex;
	CheckWallCollisions(fwd_x, fwd_y, back_x, back_y, isDoor, doorIndex);

	_playerFwdVelocity = Granite::Vector2f(fwd_x == 0 ? 2.0f * _deltaTime : 0, fwd_y == 0 ? 2.0f * _deltaTime : 0);
	_playerBwdVelocity = Granite::Vector2f(back_x == 0 ? -2.0f * _deltaTime : 0, back_y == 0 ? -2.0f * _deltaTime : 0);

	if (_keyTimer > _keyCooldownTime)
	{
		if (_wantsToDraw2Dmap)
		{
			_draw2Dmap = !_draw2Dmap;
			_wantsToDraw2Dmap = false;
		}

		_keyTimer = 0.0f;
	}
	else
	{
		_keyTimer += GraniteDeltaTime;
	}
}

void Stein::Render()
{
	// Draw temporary ceiling
	GraniteFrameBuffer.Clear(Granite::Color::Granite());

	// Draw temporary floor
	GraniteFrameBuffer.DrawRect(530, 0, 530, _map.scaledHeight() / 2 - 3, Granite::Color(90u, 90u, 90u));

	// Perform raycasting with 60 degree FOV
	_raycaster.Raycast(_draw2Dmap);

	// Draw 2D display of map and draw the player's position and direction
	if (_draw2Dmap)
	{
		DrawMap();
		_player.DrawOnMap2D(_map.cellsize(), _map.width(), Granite::Color::Orange());
	}
}

void Stein::CheckWallCollisions(int& fwd_x, int& fwd_y, int& back_x, int& back_y, bool& isDoor, int& doorIndex)
{
	int wallCheck_x = 0;
	int wallCheck_y = 0;
	int tile_p_i = _player.x();
	int tile_p_j = _player.y();

	if (_player.dir_x() < 0)
		wallCheck_x = -1;
	else
		wallCheck_x = 1;

	if (_player.dir_y() < 0)
		wallCheck_y = -1;
	else
		wallCheck_y = 1;

	int tile_add_i = (_player.x() + 0.3f * wallCheck_x);
	int tile_sub_i = (_player.x() - 0.3f * wallCheck_x);
	int tile_add_j = (_player.y() + 0.3f * wallCheck_y);
	int tile_sub_j = (_player.y() - 0.3f * wallCheck_y);

	fwd_x = _walls[tile_p_j * _map.width() + tile_add_i];
	fwd_y = _walls[tile_add_j * _map.width() + tile_p_i];
	back_x = _walls[tile_p_j * _map.width() + tile_sub_i];
	back_y = _walls[tile_sub_j * _map.width() + tile_p_i];

	doorIndex = tile_add_j * _map.width() + tile_add_i;
	
	if (_wantsToOpenDoor && _walls[doorIndex] == 3)
	{
		_map.SetWallsAt(doorIndex, 0);
		_walls[doorIndex] = 0;

		_wantsToOpenDoor = false;
	}
}

void Stein::DrawMap()
{
	for (int i = 0; i < _map.width(); i++)
	{
		for (int j = 0; j < _map.height(); j++)
		{
			// Pick a wall color
			Granite::Color wallColor = Granite::Color::Black();
			int index = j * _map.width() + i;
			if (index >= 0 && index < _map.width() * _map.height() && _walls[index] > 0)
				wallColor = Granite::Color::White();

			GraniteFrameBuffer.DrawRect(i * _map.cellsize() + 1, j * _map.cellsize() + 1, _map.cellsize() - 1, _map.cellsize() - 1, wallColor);
		}
	}
}