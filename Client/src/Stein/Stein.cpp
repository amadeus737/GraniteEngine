void Stein::Input()
{
	if (_window.keyboard.KeyIsPressed('W') || _window.keyboard.KeyIsPressed(VK_UP))
		_player.Move(_playerFwdVelocity);

	if (_window.keyboard.KeyIsPressed('S') || _window.keyboard.KeyIsPressed(VK_DOWN))
		_player.Move(_playerBwdVelocity);

	if (_window.keyboard.KeyIsPressed('A') || _window.keyboard.KeyIsPressed(VK_LEFT))  _player.Rotate(-_deltaTime);

	if (_window.keyboard.KeyIsPressed('D') || _window.keyboard.KeyIsPressed(VK_RIGHT)) _player.Rotate(_deltaTime);
}

void Stein::Update()
{
	// Check walls for collisions
	int fwd_x, fwd_y, back_x, back_y;
	CheckWallCollisions(fwd_x, fwd_y, back_x, back_y);

	_playerFwdVelocity = Granite::Vector2f(fwd_x == 0 ? 2.0f * _deltaTime : 0, fwd_y == 0 ? 2.0f * _deltaTime : 0);
	_playerBwdVelocity = Granite::Vector2f(back_x == 0 ? -2.0f * _deltaTime : 0, back_y == 0 ? -2.0f * _deltaTime : 0);
}

void Stein::Render()
{
	// Draw temporary ceiling
	_frameBuffer.Clear(Granite::Color::Granite());

	// Draw temporary floor
	_frameBuffer.DrawRect(530, 0, 530, _map.scaledHeight() / 2 - 3, Granite::Color(90u, 90u, 90u));

	// Draw 2D display of map and draw the player's position and direction
	DrawMap();
	_player.DrawOnMap2D(_map.cellsize(), _map.width(), Granite::Color::Orange());

	// Perform raycasting with 60 degree FOV
	_raycaster.Raycast(60.0f);
}

void Stein::CheckWallCollisions(int& fwd_x, int& fwd_y, int& back_x, int& back_y)
{
	int wallCheck_x = 0;
	int wallCheck_y = 0;
	int tile_p_i = _player.x();
	int tile_p_j = _player.y();

	if (_player.dir_x() < 0)
		wallCheck_x = -1;
	else
		wallCheck_x = 1;

	if (_player.dir_y() < 0)
		wallCheck_y = -1;
	else
		wallCheck_y = 1;

	int tile_add_i = (_player.x() + 0.3f * wallCheck_x);
	int tile_sub_i = (_player.x() - 0.3f * wallCheck_x);
	int tile_add_j = (_player.y() + 0.3f * wallCheck_y);
	int tile_sub_j = (_player.y() - 0.3f * wallCheck_y);

	fwd_x = _walls[tile_p_j * _map.width() + tile_add_i];
	fwd_y = _walls[tile_add_j * _map.width() + tile_p_i];
	back_x = _walls[tile_p_j * _map.width() + tile_sub_i];
	back_y = _walls[tile_sub_j * _map.width() + tile_p_i];
}

void Stein::DrawMap()
{
	for (int i = 0; i < _map.width(); i++)
	{
		for (int j = 0; j < _map.height(); j++)
		{
			// Pick a wall color
			Granite::Color wallColor = Granite::Color::Black();
			int index = j * _map.width() + i;
			if (index >= 0 && index < _map.width() * _map.height() && _walls[index] > 0)
				wallColor = Granite::Color::White();

			_frameBuffer.DrawRect(i * _map.cellsize() + 1, j * _map.cellsize() + 1, _map.cellsize() - 1, _map.cellsize() - 1, wallColor);
		}
	}
}